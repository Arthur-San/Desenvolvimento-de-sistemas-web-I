
function somar(){
    //inicio do armazenamento
    var x;
    var y;
    var soma;
    //fim do armazenamento


    //inicio da entrada de dados
    x = parseInt(window.prompt("informe o valor de X: "));
    y = parseFloat(window.prompt("informe o valor de Y: "));
    //fim da entrada de dados


    //inicio do processamento
    soma = x + y;
    //fim do processamento


    //inicio da saida de dados
    window.alert("A soma entre X e Y é: " + soma);
    //fim da saida de dados
}

